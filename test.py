# 증상-약 데이터 (딕셔너리 형태)
medicine_data = {
    "속쓰림 / 위산 역류": {
        "성분": "제산제 (알마게이트, 수산화마그네슘, 알루미늄 수산화물 등)",
        "추천 약": ["겔포스", "알마겔"]
    },
    "더부룩함 / 소화불량": {
        "성분": "소화효소제 (판크레아틴, 디아스타제 등)",
        "추천 약": ["훼스탈", "베나치오", "베아제"]
    },
    "과식 후 체함": {
        "성분": "소화효소제 + 생약제",
        "추천 약": ["베아제", "평위산(생약제)", "활명수"]
    },
    "가스참 / 트림 / 방귀 과다": {
        "성분": "소포제 (시메티콘 성분)",
        "추천 약": ["이지엔6 가스정", "가스활명수"]
    },
    "메스꺼움 / 구역질": {
        "성분": "생약성 건위제",
        "추천 약": ["위청수", "활명수"]
    }
}

# 앱 제목
st.title("💊 증상별 소화제 추천 앱")

st.write("증상을 선택하면, 알맞은 일반의약품 소화제를 추천해드립니다.")

# 증상 선택
symptom = st.selectbox("증상을 선택하세요 👇", list(medicine_data.keys()))

# 선택한 증상에 따른 결과 표시
if symptom:
    st.subheader(f"🩺 선택한 증상: {symptom}")
    st.write(f"**추천 성분**: {medicine_data[symptom]['성분']}")
    st.write("**추천 약품**:")
    for med in medicine_data[symptom]['추천 약']:
        st.markdown(f"- {med}")

